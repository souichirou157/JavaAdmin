/*USED APPLICATION VIEWS*/



CREATE VIEW  SUBJECT_SCORE  

AS SELECT ID , SCIENCE_SCORE , TECHNOLOGY_SCORE ,ENGINEERING_SCORE  , MATH_SCORE 

FROM STUDENT_INFORMATION


//連絡先
CREATE VIEW  CONTACT_ADDRESS   

AS  SELECT ID ,TELEPHONE_NUMBER,MAIL_ADDRESS

FROM STUDENT_INFORMATION;





/*出席時間を管理する*/
CREATE VIEW ATTENDED_ADMIN AS 

SELECT ID,ATTENDED_TIME 

FROM  STUDENT_INFORMATION;



CREATE VIEW  ALERT_SENDER  AS

SELECT ID,POST_NUMBER,ADDRESS FROM STUDENT_INFORMATION

WHERE  ATTENDED_TIME  < (
 
 SELECT DATEDIFF(NOW(),'2024-04-10 00:00:00')*0.8 AS ALERT_FLAG
 

);


